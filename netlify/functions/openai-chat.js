export default async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { status: 204, headers: corsHeaders() });
  }

  if (req.method !== "POST") {
    return new Response(JSON.stringify({ error: "Method not allowed" }), {
      status: 405, headers: corsHeaders(),
    });
  }

  const OPENAI_API_KEY = process.env.OPENAI_API_KEY;
  if (!OPENAI_API_KEY) {
    return new Response(JSON.stringify({ error: "OpenAI API key not configured" }), {
      status: 500, headers: corsHeaders(),
    });
  }

  try {
    const body = await req.json();
    const { type, tabId, message, history } = body;

    // ── 섹션 설명 요청 (GPT 호출 없이 고정 스크립트 반환) ──
    if (type === "tab_explain") {
      const script = SECTION_SCRIPTS[tabId];
      if (script) {
        return new Response(JSON.stringify({ reply: cleanForDisplay(script), ttsReply: script, action: "none", tabId: null }), {
          status: 200, headers: corsHeaders(),
        });
      }
      return new Response(JSON.stringify({ reply: "해당 섹션에 대한 설명을 찾을 수 없습니다.", ttsReply: "해당 섹션에 대한 설명을 찾을 수 없습니다.", action: "none", tabId: null }), {
        status: 200, headers: corsHeaders(),
      });
    }

    // ── 일반 채팅 요청 (OpenAI API) ──
    const messages = [
      { role: "system", content: SYSTEM_PROMPT },
      ...(history || []).slice(-10),
      { role: "user", content: message },
    ];

    const res = await fetch("https://api.openai.com/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "Authorization": `Bearer ${OPENAI_API_KEY}`,
      },
      body: JSON.stringify({
        model: "gpt-4o-mini",
        messages,
        max_tokens: 400,
        temperature: 0,
        response_format: { type: "json_object" },
      }),
    });

    const data = await res.json();
    if (data.error) {
      return new Response(JSON.stringify({ error: data.error.message }), {
        status: 400, headers: corsHeaders(),
      });
    }

    const rawReply = data.choices?.[0]?.message?.content || '{"reply": "죄송합니다. 답변을 생성하지 못했습니다.", "action": "none", "tabId": null}';

    let parsedReply;
    try {
      parsedReply = JSON.parse(rawReply);
    } catch {
      parsedReply = { reply: rawReply, action: "none", tabId: null };
    }

    // ── 키워드 기반 navigate 폴백 ──
    if (parsedReply.action === "none" || !parsedReply.action) {
      const msgLower = (message || "").toLowerCase().replace(/\s/g, "");
      const greetings = ["안녕", "고마워", "감사", "반가워", "잘가", "바이바이"];
      const isGreeting = greetings.some(g => msgLower.includes(g));

      if (!isGreeting && msgLower.length > 1) {
        let bestMatch = null;
        for (const [sectionId, info] of Object.entries(SECTION_INFO)) {
          for (const kw of info.keywords) {
            const kwNorm = kw.replace(/\s/g, "");
            if (msgLower.includes(kwNorm)) {
              const score = kwNorm.length;
              if (!bestMatch || score > bestMatch.score) {
                bestMatch = { id: sectionId, score };
              }
            }
          }
        }
        if (bestMatch) {
          parsedReply.action = "navigate";
          parsedReply.tabId = bestMatch.id;
        }
      }
    }

    // ── 채팅 버블용: 발음 텍스트 → 자연스러운 표시용 텍스트 ──
    const displayReply = parsedReply.reply ? cleanForDisplay(parsedReply.reply) : parsedReply.reply;
    // ── TTS용: 발음 최적화 후처리 ──
    const ttsReply = parsedReply.reply ? applyTtsPostProcessing(parsedReply.reply) : parsedReply.reply;

    return new Response(JSON.stringify({
      reply: displayReply,
      ttsReply,
      action: parsedReply.action || "none",
      tabId: parsedReply.tabId || null,
    }), {
      status: 200, headers: corsHeaders(),
    });
  } catch (err) {
    return new Response(JSON.stringify({ error: err.message, reply: "오류가 발생했습니다.", ttsReply: "오류가 발생했습니다.", action: "none", tabId: null }), {
      status: 500, headers: corsHeaders(),
    });
  }
};

function corsHeaders() {
  return {
    "Content-Type": "application/json",
    "Access-Control-Allow-Origin": "*",
    "Access-Control-Allow-Methods": "POST, OPTIONS",
    "Access-Control-Allow-Headers": "Content-Type",
  };
}
